# complex()

# Convert a single string or number to a complex number,
# or create a complex number from real and imaginary parts.

print(complex('+1.23'))

print(complex('-4.5j'))

print(complex('-1.23+4.5j'))

print(complex('\t( -1.23+4.5J )\n'))

print(complex('-Infinity+NaNj'))

print(complex(1.23))

print(complex(imag=-4.5))

print(complex(-1.23, 4.5))

